create table Article (
    id int8 generated by default as identity,
    content varchar(5000),
    postedAt timestamp,
    title varchar(255),
    primary key (id)
);

create table Article_authorIds (
    Article_id bigint not null,
    authorIds bigint
);

create table User_data (
    id varchar(255),
    name varchar(255),
    email varchar(255),
    password varchar(255),
    gender varchar(10),
    primary key (id)
);

create table hotels (
	hotelid int8 generated by default as identity,
	hotelname varchar(50),
	rating varchar(20),
	location varchar(50),
	price int
);

create table booking_data (
	id int8 generated by default as identity,
	email varchar(70),
	hotelname varchar(50),
	name varchar(100),
	age int,
	checkindate varchar(100),
	checkoutdate varchar(100),
	phone varchar(20),
	primary key(id)
);

